public class Player{
    public static Object name;
    public static int lang;
    public static int choice = 123;
    public Player() {
        this.name = name;
        this.lang = lang;
        this.choice = choice;
    }

    public String getName() {
        return name.toString();
    }

    public int getLang() {
        return lang;
    }

    public int getChoice() {
        return choice;
    }


}
